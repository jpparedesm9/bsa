package com.cobiscorp.ecobis.customer.services.dtos;

public class IdCustomerDataResponse {
	
	private String code;
	private String description;
	private String mask;
	private String typePerson;
	private String province;
	private String aperquick;
	private String blocks;
	private String nationality ;
	private String digit ;
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getMask() {
		return mask; 
	}
	public void setMask(String mask) {
		this.mask = mask;
	}
	public String getTypePerson() {
		return typePerson;
	}
	public void setTypePerson(String typePerson) {
		this.typePerson = typePerson;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getAperquick() {
		return aperquick;
	}
	public void setAperquick(String aperquick) {
		this.aperquick = aperquick;
	}
	public String getBlocks() {
		return blocks;
	}
	public void setBlocks(String blocks) {
		this.blocks = blocks;
	}
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public String getDigit() {
		return digit;
	}
	public void setDigit(String digit) {
		this.digit = digit;
	}



}
