package com.cobiscorp.mobile.model.fingerid;

public class PersonalINEData {
    private String ocr;
    private String cic;
    private String name;
    private String paternName;
    private String maternName;
    private String yearRecord;
    private String yearEmision;
    private String numberEmision;
    private String cveElector;
    private String curp;

    public String getOcr() {
        return ocr;
    }
    public void setOcr(String ocr) {
        this.ocr = ocr;
    }

    public String getCic() {
        return cic;
    }
    public void setCic(String cic) {
        this.cic = cic;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getPaternName() {
        return paternName;
    }
    public void setPaternName(String paternName) {
        this.paternName = paternName;
    }

    public String getMaternName() {
        return maternName;
    }
    public void setMaternName(String maternName) {
        this.maternName = maternName;
    }

    public String getYearRecord() {
        return yearRecord;
    }
    public void setYearRecord(String yearRecord) {
        this.yearRecord = yearRecord;
    }

    public String getYearEmision() {
        return yearEmision;
    }
    public void setYearEmision(String yearEmision) {
        this.yearEmision = yearEmision;
    }

    public String getNumberEmision() {
        return numberEmision;
    }
    public void setNumberEmision(String numberEmision) {
        this.numberEmision = numberEmision;
    }

    public String getCveElector() {
        return cveElector;
    }
    public void setCveElector(String cveElector) {
        this.cveElector = cveElector;
    }

    public String getCurp() {
        return curp;
    }
    public void setCurp(String curp) {
        this.curp = curp;
    }
}
