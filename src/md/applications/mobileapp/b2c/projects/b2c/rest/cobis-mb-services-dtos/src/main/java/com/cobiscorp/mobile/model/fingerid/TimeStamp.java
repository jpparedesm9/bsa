package com.cobiscorp.mobile.model.fingerid;

public class TimeStamp {
    private Object momento;
    private Object indice;
    private Object numeroSerie;

    public Object getMomento() {
        return momento;
    }
    public void setMomento(Object momento) {
        this.momento = momento;
    }

    public Object getIndice() {
        return indice;
    }
    public void setIndice(Object indice) {
        this.indice = indice;
    }
	public Object getNumeroSerie() {
		return numeroSerie;
	}
	public void setNumeroSerie(Object numeroSerie) {
		this.numeroSerie = numeroSerie;
	}

}
