package com.cobiscorp.notification.client.dtos;

public class AlertData {
	private String alertDataKey;
	private String alertDataValue;
	private String alertDataType;
	public String getAlertDataKey() {
		return alertDataKey;
	}
	public void setAlertDataKey(String alertDataKey) {
		this.alertDataKey = alertDataKey;
	}
	public String getAlertDataValue() {
		return alertDataValue;
	}
	public void setAlertDataValue(String alertDataValue) {
		this.alertDataValue = alertDataValue;
	}
	public String getAlertDataType() {
		return alertDataType;
	}
	public void setAlertDataType(String alertDataType) {
		this.alertDataType = alertDataType;
	}
	
}
