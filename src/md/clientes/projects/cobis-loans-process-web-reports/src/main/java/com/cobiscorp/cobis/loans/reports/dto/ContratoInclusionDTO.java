package com.cobiscorp.cobis.loans.reports.dto;

public class ContratoInclusionDTO {
	private String officeAddress;
	private String city;
	private String date;
	private String numeroReca;
	private String representative;
	private String amountLetter;
	private String amount;
	private String customerName;
	private String accountName;
	public String getOfficeAddress() {
		return officeAddress;
	}
	public void setOfficeAddress(String officeAddress) {
		this.officeAddress = officeAddress;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getNumeroReca() {
		return numeroReca;
	}
	public void setNumeroReca(String numeroReca) {
		this.numeroReca = numeroReca;
	}
	public String getRepresentative() {
		return representative;
	}
	public void setRepresentative(String representative) {
		this.representative = representative;
	}
	public String getAmountLetter() {
		return amountLetter;
	}
	public void setAmountLetter(String amountLetter) {
		this.amountLetter = amountLetter;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getAccountName() {
		return accountName;
	}
	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}
}
